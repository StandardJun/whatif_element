// 원소 추가 정보 가져오기
export function getElementExtras(symbol: string): ElementExtras {
  if (elementExtrasData[symbol]) {
    return elementExtrasData[symbol];
  }

  const element = elements.find(e => e.symbol === symbol);
  if (!element) {
    return {
      history: '이 원소에 대한 발견 역사 정보가 준비 중입니다.',
      uses: ['다양한 산업 분야에서 활용됩니다'],
      funFacts: ['독특한 특성을 가진 원소입니다'],
    };
  }

  const defaults = categoryDefaults[element.category];
  if (defaults) {
    return {
      history: `${element.nameKo}은(는) ${defaults.history}`,
      uses: defaults.uses,
      funFacts: defaults.funFacts,
    };
  }

  return {
    history: '이 원소에 대한 정보가 준비 중입니다.',
    uses: ['다양한 분야에서 활용됩니다'],
    funFacts: ['독특한 특성을 가진 원소입니다'],
  };
}

